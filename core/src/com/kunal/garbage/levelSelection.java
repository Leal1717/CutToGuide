public class levelSelection implements Screen {

    MainGame game;
    OrthographicCamera cam;
    Viewport port;
    Texture levelArea1;

    Sprite l1;

    public levelSelection(MainGame game) {
        this.game = game;

        cam = new OrthographicCamera();
        cam.setToOrtho(false, AllVariables.WIDTH, AllVariables.HEIGHT);

        port = new FitViewport(AllVariables.WIDTH, AllVariables.HEIGHT, cam);

        port.apply();

        //levelArea1 = new Texture(Gdx.files.internal("levelSelection/p1.png"));
        l1 = new Sprite(new Texture(Gdx.files.internal("levelSelection/p1.png")));
        l1.setSize(AllVariables.WIDTH,AllVariables.HEIGHT);
        l1.setPosition(0,0);

        AllVariables.inpM = (float)Gdx.graphics.getHeight()/AllVariables.HEIGHT;
        AllVariables.witdth_translation =  (Gdx.graphics.getWidth() - ((Gdx.graphics.getHeight()*16)/9))/2;


    }

    @Override
    public void show() {

    }

    @Override
    public void render(float delta) {
        Gdx.gl.glClearColor(0f, 0f, 0f, 0f);
        Gdx.gl.glClear(GL20.GL_COLOR_BUFFER_BIT);

        AllVariables.batch.setProjectionMatrix(cam.combined);

        AllVariables.batch.begin();
        /*AllVariables.batch.draw(levelArea1,0,0, Gdx.graphics.getWidth()3.
            , Gdx.graphics.getHeight());
        */
        l1.draw(AllVariables.batch);
        AllVariables.batch.end();


    }

    @Override
    public void resize(int width, int height) {
        port.update(width, height);
        cam.update();
        AllVariables.inpM = (float)Gdx.graphics.getHeight()/AllVariables.HEIGHT;
        AllVariables.witdth_translation =  (Gdx.graphics.getWidth() - ((Gdx.graphics.getHeight()*16)/9))/2;
    }

    @Override
    public void pause() {

    }

    @Override
    public void resume() {

    }

    @Override
    public void hide() {

    }

    @Override
    public void dispose() {

    }
}
